declare(strict_types=1);

namespace App\Core;

final class Language
{
    /** @var list<string> */
    private const SUPPORTED = ['en', 'nl', 'de', 'fr', 'es'];

    /** @return list<string> */
    public static function supported(): array
    {
        return self::SUPPORTED;
    }

    public static function normalize(?string $language): string
    {
        $code = is_string($language) ? strtolower(trim($language)) : 'en';
        return in_array($code, self::SUPPORTED, true) ? $code : 'en';
    }

    /** @return array<string,string> */
    public static function labels(): array
    {
        return [
            'en' => 'English',
            'nl' => 'Nederlands',
            'de' => 'Deutsch',
            'fr' => 'Français',
            'es' => 'Español',
        ];
    }

    public static function current(): string
    {
        return self::normalize($_SESSION['user_language'] ?? 'en');
    }

    public static function setCurrent(string $language): void
    {
        $_SESSION['user_language'] = self::normalize($language);
    }

    public static function translate(string $key, ?string $language = null): string
    {
        $lang = self::normalize($language ?? self::current());
        $messages = self::translations();

        if (isset($messages[$lang][$key])) {
            return $messages[$lang][$key];
        }

        return $messages['en'][$key] ?? $key;
    }

